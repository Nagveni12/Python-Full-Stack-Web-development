class SeatBooking:
    def __init__(self, total_seats):
        self.total_seats = total_seats
        self.current = 1

    def __iter__(self):
        return self

    def __next__(self):
        if self.current <= self.total_seats:
            seat = self.current
            self.current += 1
            return seat
        else:
            raise StopIteration


booking = SeatBooking(3)

while True:
    choice = input("Book seat? (yes/no): ").lower()

    if choice == "yes":
        try:
            seat = next(booking)
            print(f"Seat {seat} booked successfully")
        except StopIteration:
            print("No seats available")
            break
    else:
        break

