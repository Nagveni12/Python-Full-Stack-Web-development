def calculate_bill():
    prices = []

    n = int(input("Enter number of items: "))

    for i in range(n):
        price = float(input(f"Enter price of item {i+1}: "))
        prices.append(price)

    gst = float(input("Enter GST %: "))

    total = sum(prices)
    gst_amount = total * gst / 100
    final = total + gst_amount

    print("Final Bill:", final)

